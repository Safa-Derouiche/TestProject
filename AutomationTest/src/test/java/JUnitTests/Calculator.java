package JUnitTests;

public class Calculator {
	public int add(int a, int b) {
	      return a + b; 
	    }
	    public int subtract(int a, int b) {
	      return a - b;
	    }
	    public int multiply(int x, int y) {
	      return x * y;
	    }
	    public int divide(int a, int b) {
	      return a / b;
	    }
	    public int modulo(int a, int b) {
	      return a % b;
	    }

		
}
